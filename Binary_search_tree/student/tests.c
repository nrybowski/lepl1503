/**
 * Fonction d'aide, permet de libérer la mémoire prise par l'arbre.
 * @param currentNode le noeud root de l'arbre à libérer.
 * @return le nombre de noeuds qui ont été "free"
 */
int destroyTree(struct node *currentNode) {
    if (currentNode == NULL) return 0;

    int tot = destroyTree(currentNode->left) + destroyTree(currentNode->right);
    free(currentNode);

    return tot + 1;
}

/**
 * Créer un arbre balancé contenant toutes les valeurs comprisent dans l'interval [left, right]
 * @param left la valeur definissant le début de l'interval
 * @param right la valeur definissant la fin de l'interval
 * @param root le noeud "root" de l'arbre à créer
 */
void makeBalancedTree(int left, int right, struct node **root) {
    if (left > right) return;
    else if (left == right) {
        *root = addCorrect(*root, left);
    } else {
        int mid = (left + right) / 2;
        *root = addCorrect(*root, mid);

        makeBalancedTree(left, mid - 1, root);
        makeBalancedTree(mid + 1, right, root);
    }
}

int areTreesEquals(struct node *root, struct node *correctRoot) {

    if (root == NULL) {
        if (correctRoot == NULL) return 1;
        else return 0;
    } else if (correctRoot == NULL) return 0;

    if (root->value != correctRoot->value) return 0;

    if (areTreesEquals(root->left, correctRoot->left) == 0 ||
        areTreesEquals(root->right, correctRoot->right) == 0)
        return 0;

    return 1;
}

